    <h3 class="font-weight-bold text-center">PERFORMANCE TESTING TYPES OF SERVICES WE OFFER</h3>
    <h6 class="font-weight-bold px-5">Load Testing</h6>
    <p class="text-justify px-5 pb-1">
    Our team at <b>Techasoft</b>, checks the system behavior under expected load variations which help us to identify a number of multiple connections that the system can handle while delivering a response time. In addition to that, we also detect system bottlenecks and determine which system element is causing them and try to fix them up in no time.
    </p>
    <h6 class="font-weight-bold px-5">Scalability Testing</h6>
    <p class="text-justify px-5 pb-1">
    We measure the capability of a system to scale as more load is applied to it and also, help to understand that at which point exactly the system stops scaling and identify the reasons for that. The results of scalability testing can be very well used to plan for the future growth and avoid over investment in hardware.
    </p>
    <h6 class="font-weight-bold px-5">Soak Testing</h6>
    <p class="text-justify px-5 pb-1">
    Soak Testing is performed to check if the application under test can sustain the continuous production loads or not. Soak testing helps to identify issues relating to memory allocation, log file handles, database resource utilization, and much more.
    </p>
    <h6 class="font-weight-bold px-5">Spike Testing</h6>
    <p class="text-justify px-5 pb-1">
    Spike testing is really pivotal to performance testing type to make sure that extreme increase and decrease of production load is sustained by the system and to determine the behavior of the software application when it receives extreme variations in the traffic.
    </p>
    <h6 class="font-weight-bold px-5">Concurrency Testing
    </h6>
    <p class="text-justify px-5 pb-1">
    This type of testing lets us examine how your application works when multiple users log into it. Concurrency testing also helps identify risks such as server response time, deadlocks and throughput associated with the concurrency.
    </p>
    <h6 class="font-weight-bold px-5">Configuration Testing
    </h6>
    <p class="text-justify px-5 pb-1">
    This type of testing helps in testing the system with a variety of supported software and hardware configurations.
    </p>
    <h6 class="font-weight-bold px-5">Isolation Testing</h6>
    <p class="text-justify px-5 pb-1">
    Isolation testing is used to identify the defects easily by breaking down the system into various isolated modules and then knowing where exactly the problem is.
    </p>
    <h6 class="font-weight-bold px-5">Stability Testing</h6>
    <p class="text-justify px-5 pb-1">
    Techasoft checks whether the system remains stable over an extended period of time, including 24 by 7 operation and this helps evaluate the risk of downtime to plan all the maintenance activities.
    </p>
    <h6 class="font-weight-bold px-5">Volume Testing
    </h6>
    <p class="text-justify px-5 pb-1">
    Our team of experts identifies the capacity of the system or app whether it is normal or heavy and this identification further helps us to resolve and identify all the problems that are likely to occur with a large amount of data in it. Volume testing also measures the response time of the system when data input is very large in the system or application. Another key purpose of the volume testing is to make sure that all the data entered for the purpose of the test is considered in the process and not lost.
    </p>
    <h6 class="font-weight-bold px-5">Stress Testing
    </h6>
    <p class="text-justify px-5 pb-1">
    We identify the maximum operating capacity which the system can handle in terms of extreme load and then we proceed to check whether it will perform sufficiently if the current load goes above the expected maximum or fail. This really helps to plan the potential outrages that can result in new system connections because of the extreme load.
